#!/bin/bash
set -e

# Скрипт для автоматического обновления и перезапуска бота

echo "🚀 Начинаю процесс деплоя..."

# 1. Получаем последние изменения из репозитория
echo "📥 Получение обновлений из git..."
git pull

# 2. Пересобираем и запускаем контейнеры
echo "🏗️ Пересборка и запуск контейнеров..."
docker-compose up -d --build

# 3. Очистка неиспользуемых образов (опционально, для экономии места)
echo "🧹 Очистка старых образов..."
docker image prune -f

echo "✅ Деплой успешно завершен!"
